package Model;


public class Position {

    private int x;

    private int y;

    public Position () {
    }

    public int getX () {
        return x;
    }

    public void setX (int val) {
        this.x = val;
    }

    public int getY () {
        return y;
    }

    public void setY (int val) {
        this.y = val;
    }

}

